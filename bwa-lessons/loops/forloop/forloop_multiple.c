#include <stdio.h>

/**
 * main - main entry
 * Description: Using for loops to create multiple of numbers using a prompt.
 * Return: Always 0
 */

int main()
{
	int num, mul;

	printf("Enter a number: ");
	scanf("%d", &num);

	for (num == num; num<=4; num++)
	{

	for (mul = 1; mul <=10; mul++)
	{
		printf("\t%d", (num * mul));

	}

	printf("\n");
	}

	return 0;
}
